package com.itshixun.model;

public class StaffInfo {
	private String staffName;
	private int staffId;
	private int deptNo;
	private String staffPos;
	private String staffPosName;
	private String staffSex;
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public int getStaffId() {
		return staffId;
	}
	public void setStaffId(int staffId) {
		this.staffId = staffId;
	}
	public int getDeptNo() {
		return deptNo;
	}
	public void setDeptNo(int deptNo) {
		this.deptNo = deptNo;
	}
	public String getStaffPos() {
		return staffPos;
	}
	public void setStaffPos(String staffPos) {
		this.staffPos = staffPos;
	}
	public String getStaffSex() {
		return staffSex;
	}
	public void setStaffSex(String staffSex) {
		this.staffSex = staffSex;
	}
	public String getStaffPosName() {
		return staffPosName;
	}
	public void setStaffPosName(String staffPosName) {
		this.staffPosName = staffPosName;
	}
	

}
